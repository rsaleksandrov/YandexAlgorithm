"""
Алла успешно справилась с предыдущим заданием, и теперь ей дали новое.
На этот раз список рёбер ориентированного графа надо переводить в матрицу
смежности. Конечно же, Алла попросила вас помочь написать программу для этого.

Формат ввода
В первой строке дано число вершин n (1 ≤ n ≤ 100) и
число рёбер m (1 ≤ m ≤ n(n-1)). В следующих m строках заданы ребра в виде
пар вершин (u,v), если ребро ведет от u к v.

Формат вывода
Выведите матрицу смежности n на n. На пересечении i-й строки и j-го столбца
стоит единица, если есть ребро, ведущее из i в j.
"""

n, m = list(map(int, input().split()))
iData = [None] * n

for i in range(m):
    u, v = list(map(int, input().split()))
    if iData[u - 1] is None:
        iData[u - 1] = [0] * n
    iData[u - 1][v - 1] = 1

for i in range(n):
    if iData[i] is None:
        print('0 ' * n)
    else:
        print(*iData[i])
